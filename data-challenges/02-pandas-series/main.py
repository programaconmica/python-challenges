# Desafio 2: Hacer una serie de Pandas a partir de una lista y mostrarla.

import pandas as pd

lista = [1, 7, 2]

serie = pd.Series(lista)

print(serie)

print(serie[0])

# Explicación: Este desafío es una introducción básica a las Series de Pandas, que son una estructura de datos unidimensional similar a un array o una lista. Aprenderás a crear una Serie a partir de una lista y a imprimir su contenido.
