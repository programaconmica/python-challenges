# Desafio 91: Hacer un programa que convierta texto a mayusculas y minusculas

texto = input("Ingresa un texto: ")
print(f"Mayúsculas: {texto.upper()}")
print(f"Minúsculas: {texto.lower()}")

# Explicación: El programa toma un texto del usuario y muestra su versión en mayúsculas y minúsculas 
# usando los métodos upper() y lower().
